          <h3 id="js">Original JavaScript Component</h3>
          <p>The original JavaScript components added to this website are functions called showPic and closePic.</p>
          <p>These components are responsible for displaying bigger versions of the thumbnail images (that are in almost all pages of this website) and closing them. This event happens when a thumbnail image is clicked.</p>
          <p>I added a different opacity on hover for these images in order to make easier to the visitor to understand the images are clickable.</p>
          <p>I also added another function with JavaScript called showMenu. It shows a hidden menu on Culture&amp;Art->Museums, Food->Bakeries and Sightseeing->Markets pages. It has a small arrow that indicates the menu will appear under it.</p>
            
